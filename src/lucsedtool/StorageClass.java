public class StorageClass {
    private String name;
    private String type;

    /**
     * @return the nome
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the nome to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the tipo
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the tipo to set
     */
    public void setType(String type) {
        this.type = type;
    }
}
